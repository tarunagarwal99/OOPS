#include<iostream>
using namespace std;
int main(int argc, char const *argv[])
{
    // for loop
    for (int i = 0; i < 40; i++)
    {
        cout<<i<<endl;

    }
    //while loop
    int i=1;
    while(i<=40){
        cout<<i<<endl;
        i++;

    }
    // do while loop
    int i=1;
    do{
        cout<<i<<endl;
        i++;

    } while(i<=40);


    
    return 0;
}
